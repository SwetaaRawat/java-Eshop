    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        if(!validateInputs()){
            JOptionPane.showMessageDialog(null,"Please fill all the fields!","ERROR!",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        try{
        EmployeePojo emp = new EmployeePojo();
        emp.setEmpid(txtEmpId.getText());
        emp.setEmpname(txtEmpName.getText());
        emp.setJob(jcJob.getSelectedItem().toString());
        emp.setSalary(Double.parseDouble(txtSalary.getText()));
        boolean result = EmployeeDAO.addEmployee(emp);
        if(!result){
          JOptionPane.showMessageDialog(null,"Record Not Inserted!","ERROR!",
                    JOptionPane.ERROR_MESSAGE);
          return;
        }
        JOptionPane.showMessageDialog(null,"Record added successfully!","Success!",
                    JOptionPane.INFORMATION_MESSAGE);
        clearText();
        loadEmpId();
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"DB Error!","Erros!",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
        catch(NumberFormatException ex){
            JOptionPane.showMessageDialog(null,"Please input numaric value for salary!","Erros!",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
        
    }//GEN-LAST:event_btnAddActionPerformed

